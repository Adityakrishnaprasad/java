package SingleDimensionalArrays;

public class ReversingArrayValues {



    public static void main(String[] args) {

        int a[] = {23, 34, 48, 56};
        for (int i=a.length-1;i>=0;i--) {
            System.out.println(a[i]);
        }
        System.out.println("............................");
        System.out.print("length of an array is : "+a.length);


    }
}
